--[[
Windows
	Border
		The border of the window, if it has any
		If resizable, then Drag to Resize
	TitleBar
		Usually on the top of the window
		Close,Mininize,Resize/Maximize Buttons
		Drag to Reposition
	MenuBar
		List of menus
	ToolBar
		List of tools as buttons or other shenanigans for quick access
	Tabs
	ScrollBars
		Vertical/Horizontal
		On either or both sides
		With or without their own resize corners for zooming in on the content area
	ContentArea
		The viewport, basically.
	StatusBar
		Usually on the bottom of the window
		Provides information about the contents of the window
	ResizeCorner
		Drag to Resize the window
	ContextMenu
		Used by both the menubar (pull-down menu), and by itself.
--]]

